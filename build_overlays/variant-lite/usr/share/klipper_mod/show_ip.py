def get_signal_bars():
    """Read wlan0 link level from /proc/net/wireless; return '1bar'/'2bar'/'3bar' or None."""
    try:
        with open('/proc/net/wireless') as f:
            for line in f:
                if 'wlan0' in line:
                    level = int(float(line.split()[3].rstrip('.')))
                    if level >= -55:
                        return '3bar'
                    if level >= -70:
                        return '2bar'
                    return '1bar'
    except (OSError, ValueError, IndexError):
        pass
    return None


def get_iface_ip(ifname):
    """Return the IPv4 address assigned to a named interface, or None."""
    SIOCGIFADDR = 0x8915
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        try:
            data = fcntl.ioctl(s.fileno(), SIOCGIFADDR,
                               struct.pack('256s', ifname.encode()[:15]))
            return socket.inet_ntoa(data[20:24])
        finally:
            s.close()
    except OSError:
        return None


def get_ip(timeout=30):
    deadline = time.monotonic() + timeout
    while time.monotonic() < deadline:
        try:
            s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
            try:
                s.connect(("8.8.8.8", 80))
                ip = s.getsockname()[0]
            finally:
                s.close()
            return ip
        except OSError:
            pass
        time.sleep(1)
    return None


def get_fb_info():
    # Read physical size from the active mode string (e.g. "U:480x272p-0").
    # virtual_size can be double the physical height when the driver uses
    # page-flipping, which would push the overlay text off-screen.
    try:
        with open('/sys/class/graphics/fb0/modes') as f:
            first_mode = f.read().strip().split('\n')[0]
        res = first_mode.split(':')[-1].split('p')[0]   # "480x272"
        w, h = map(int, res.split('x'))
    except Exception:
        with open('/sys/class/graphics/fb0/virtual_size') as f:
            w, h = map(int, f.read().strip().split(','))
    with open('/sys/class/graphics/fb0/bits_per_pixel') as f:
        bpp = int(f.read().strip())
    return w, h, bpp, w * (bpp // 8)


def encode_pixel(r, g, b, bpp):
    if bpp not in (16, 24, 32):
        raise ValueError(f"Unsupported bits-per-pixel: {bpp}")
    if bpp == 16:
        return struct.pack('<H', ((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3))
    if bpp == 24:
        return struct.pack('BBB', b, g, r)
    return struct.pack('BBBB', b, g, r, 0xFF)   # 32-bit BGRA


def _draw_icon(fb, icon_key, x0, y0, stride, ppb, bpp):
    icon_rgb = base64.b64decode(WIFI_ICONS[icon_key])
    for row in range(ICON_SIZE):
        fb.seek((y0 + row) * stride + x0 * ppb)
        line = bytearray()
        for col in range(ICON_SIZE):
            idx = (row * ICON_SIZE + col) * 3
            r, g, b = icon_rgb[idx], icon_rgb[idx + 1], icon_rgb[idx + 2]
            line += encode_pixel(r, g, b, bpp)
        fb.write(bytes(line))


def draw(text, wifi_key, eth_key, fb_w, fb_h, bpp, stride):
    ppb = bpp // 8

    # Two icons in upper right — WiFi (left) | gap | Ethernet (right)
    x0_eth  = fb_w - ICON_SIZE - CORNER_MARGIN
    x0_wifi = x0_eth - ICON_GAP - ICON_SIZE
    y0_icons = CORNER_MARGIN

    # IP address — bottom centre
    cw = 8 * SCALE
    ch = 8 * SCALE
    x0_ip = max(0, (fb_w - len(text) * cw) // 2)
    y0_ip  = fb_h - ch - 12

    fg_px = encode_pixel(*FG, bpp)
    bg_px = encode_pixel(*BG, bpp)

    with open('/dev/fb0', 'r+b') as fb:
        # Clear both icon regions in one strip
        blank_icons = bg_px * (ICON_SIZE * 2 + ICON_GAP)
        for row in range(y0_icons, y0_icons + ICON_SIZE):
            fb.seek(row * stride + x0_wifi * ppb)
            fb.write(blank_icons)

        # Clear bottom IP strip (full rows — content is above this area)
        blank_row = bg_px * fb_w
        for row in range(y0_ip - 9, fb_h):
            fb.seek(row * stride)
            fb.write(blank_row)

        # Draw both icons
        _draw_icon(fb, wifi_key, x0_wifi, y0_icons, stride, ppb, bpp)
        _draw_icon(fb, eth_key,  x0_eth,  y0_icons, stride, ppb, bpp)

        # IP address text (centred, bottom)
        for row in range(ch):
            glyph_row = row // SCALE
            line = bytearray()
            for ch_char in text:
                bits = GLYPHS.get(ch_char, GLYPHS[' '])[glyph_row]
                for col in range(8):
                    px = fg_px if (bits >> (7 - col)) & 1 else bg_px
                    line += px * SCALE
            fb.seek((y0_ip + row) * stride + x0_ip * ppb)
            fb.write(bytes(line))


# RTMGRP_IPV4_IFADDR: kernel pushes an event whenever an IPv4 address is
# added (RTM_NEWADDR) or removed (RTM_DELADDR) on any interface.  This
# covers both IP changes and wifi<->ethernet switches (each transition
# triggers an address removal on one interface and an addition on the other).
_RTMGRP_IPV4_IFADDR = 0x10


def _network_events():
    """Block until a burst of IPv4 address-change events has settled, then yield.

    A 2-second drain window coalesces related events (e.g. addr-del on wlan0
    followed immediately by addr-add on eth0 during an interface switch) so
    the display is only redrawn once per logical change.
    """
    try:
        nl = socket.socket(socket.AF_NETLINK, socket.SOCK_RAW, socket.NETLINK_ROUTE)
        nl.bind((0, _RTMGRP_IPV4_IFADDR))
    except OSError:
        return  # no netlink support — caller gets no events, one-shot mode
    try:
        while True:
            nl.recv(4096)  # block until the kernel signals a change
            deadline = time.monotonic() + 2.0
            while True:
                remaining = deadline - time.monotonic()
                if remaining <= 0:
                    break
                if not select.select([nl], [], [], remaining)[0]:
                    break
                nl.recv(4096)
            yield
    finally:
        nl.close()


def main():
    try:
        fb_w, fb_h, bpp, stride = get_fb_info()
    except OSError:
        return  # no framebuffer device — nothing to do

    def update(ip_timeout):
        ip       = get_ip(ip_timeout)
        wifi_key = get_signal_bars() or 'disconnected'
        eth_key  = 'ethernet' if get_iface_ip('eth0') else 'ethernet_disconnected'
        text     = ip if ip else '0.0.0.0'
        try:
            draw(text, wifi_key, eth_key, fb_w, fb_h, bpp, stride)
        except OSError:
            pass

    update(30)                    # boot: allow up to 30 s for initial network
    for _ in _network_events():
        update(5)                 # event: address already assigned, 5 s is generous


if __name__ == '__main__':
    main()
