def get_signal_bars():
    """Read wlan0 link level from /proc/net/wireless; return '1bar'/'2bar'/'3bar' or None."""
    try:
        with open('/proc/net/wireless') as f:
            for line in f:
                if 'wlan0' in line:
                    level = int(float(line.split()[3].rstrip('.')))
                    if level >= -55:
                        return '3bar'
                    if level >= -70:
                        return '2bar'
                    return '1bar'
    except (OSError, ValueError, IndexError):
        pass
    return None


def get_ip(timeout=30):
    deadline = time.monotonic() + timeout
    while time.monotonic() < deadline:
        try:
            s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
            try:
                s.connect(("8.8.8.8", 80))
                ip = s.getsockname()[0]
            finally:
                s.close()
            return ip
        except OSError:
            pass
        time.sleep(1)
    return None


def get_fb_info():
    # Read physical size from the active mode string (e.g. "U:480x272p-0").
    # virtual_size can be double the physical height when the driver uses
    # page-flipping, which would push the overlay text off-screen.
    try:
        with open('/sys/class/graphics/fb0/modes') as f:
            first_mode = f.read().strip().split('\n')[0]
        res = first_mode.split(':')[-1].split('p')[0]   # "480x272"
        w, h = map(int, res.split('x'))
    except Exception:
        with open('/sys/class/graphics/fb0/virtual_size') as f:
            w, h = map(int, f.read().strip().split(','))
    with open('/sys/class/graphics/fb0/bits_per_pixel') as f:
        bpp = int(f.read().strip())
    return w, h, bpp, w * (bpp // 8)


def encode_pixel(r, g, b, bpp):
    if bpp not in (16, 24, 32):
        raise ValueError(f"Unsupported bits-per-pixel: {bpp}")
    if bpp == 16:
        return struct.pack('<H', ((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3))
    if bpp == 24:
        return struct.pack('BBB', b, g, r)
    return struct.pack('BBBB', b, g, r, 0xFF)   # 32-bit BGRA


def draw(text, icon_key, fb_w, fb_h, bpp, stride):
    ppb = bpp // 8

    # Main line: WiFi icon + IP address
    cw   = 8 * SCALE
    ch   = 8 * SCALE
    gap_w  = cw // 2
    text_w = len(text) * cw
    total_w = ICON_SIZE + gap_w + text_w
    x0 = max(0, (fb_w - total_w) // 2)
    y0 = fb_h - ch - 12

    # Secondary line: port numbers, smaller and dimmer, just above the main line
    cw_s = 8 * SCALE_SMALL
    ch_s = 8 * SCALE_SMALL
    port_text = " ".join(f":{p}" for p in PORTS)
    x0_port = max(0, (fb_w - len(port_text) * cw_s) // 2)
    y0_port = y0 - ch_s - 4

    fg_px  = encode_pixel(*FG, bpp)
    dim_px = encode_pixel(*FG_DIM, bpp)
    bg_px  = encode_pixel(*BG, bpp)
    blank_row = bg_px * fb_w

    with open('/dev/fb0', 'r+b') as fb:
        # Clear the strip (extended upward to cover port line)
        for row in range(y0_port - 9, fb_h):
            fb.seek(row * stride)
            fb.write(blank_row)

        # Port numbers in dim colour
        for row in range(ch_s):
            glyph_row = row // SCALE_SMALL
            line = bytearray()
            for ch_char in port_text:
                bits = GLYPHS.get(ch_char, GLYPHS[' '])[glyph_row]
                for col in range(8):
                    px = dim_px if (bits >> (7 - col)) & 1 else bg_px
                    line += px * SCALE_SMALL
            fb.seek((y0_port + row) * stride + x0_port * ppb)
            fb.write(bytes(line))

        # WiFi status icon (pre-rasterized 24x24 RGB, composited over black)
        icon_rgb = base64.b64decode(WIFI_ICONS[icon_key])
        for row in range(ICON_SIZE):
            fb.seek((y0 + row) * stride + x0 * ppb)
            line = bytearray()
            for col in range(ICON_SIZE):
                idx = (row * ICON_SIZE + col) * 3
                r, g, b = icon_rgb[idx], icon_rgb[idx + 1], icon_rgb[idx + 2]
                line += encode_pixel(r, g, b, bpp)
            fb.write(bytes(line))

        # IP address text to the right of the icon
        text_x0 = x0 + ICON_SIZE + gap_w
        for row in range(ch):
            glyph_row = row // SCALE
            line = bytearray()
            for ch_char in text:
                bits = GLYPHS.get(ch_char, GLYPHS[' '])[glyph_row]
                for col in range(8):
                    px = fg_px if (bits >> (7 - col)) & 1 else bg_px
                    line += px * SCALE
            fb.seek((y0 + row) * stride + text_x0 * ppb)
            fb.write(bytes(line))


def main():
    try:
        fb_w, fb_h, bpp, stride = get_fb_info()
    except OSError:
        return  # no framebuffer device — nothing to do

    ip = get_ip()

    if ip:
        icon_key = get_signal_bars() or 'ethernet'
        text = ip
    else:
        icon_key = 'disconnected'
        text = "0.0.0.0"

    try:
        draw(text, icon_key, fb_w, fb_h, bpp, stride)
    except OSError:
        pass


if __name__ == '__main__':
    main()
